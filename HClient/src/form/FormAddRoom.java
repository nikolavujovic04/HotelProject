    private void validateForm() throws Exception{
        String errorMessage = "";
        jlblTypeError.setText("");
        jlblPriceError.setText("");
        jlblQuantityError.setText("");
        
        if(jRoomType.getText().isEmpty()){
            errorMessage = "Tip sobe ne sme biti prazan";
            jlblTypeError.setText(errorMessage);
        }
        
        if(jRoomPrice.getText().isEmpty()){
            errorMessage = "Cena sobe ne sme biti prazna";
            jlblPriceError.setText(errorMessage);
        }
        
        if(jRoomQuantity.getText().isEmpty()){
            errorMessage = "Kolicina sobe ne sme biti prazna";
            jlblQuantityError.setText(errorMessage);
        }
        
        if(!errorMessage.isEmpty()){
            throw new Exception("Morate popuniti sva polja");
        }
    }
