    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try {
            validateForm();
            String roomType = jRoomType.getText().trim();
            double price = Double.parseDouble(jRoomPrice.getText());
            int quantity = Integer.parseInt(jRoomQuantity.getText());
            
            Room room = new Room(roomType, price, quantity);
            Controller.getInstance().addRoom(room);
            JOptionPane.showMessageDialog(this, "Soba je uspesno uneta u sistem", "Obavestenje", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Nije moguce ubaciti sobu u sistem", "Greska!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton5ActionPerformed


